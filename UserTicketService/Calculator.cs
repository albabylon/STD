namespace UserTicketService
{
    public class Calculator
    {
        public int Multiplication(int a, int b)
        {
            return a * b;
        }

        public int Subtraction(int a, int b)
        {
            return a - b;
        }

        public int Division(int a, int b)
        {
            return a / b;
        }

        public int Add(int one, int two)
        {
            return one + two;
        }
    }
}
